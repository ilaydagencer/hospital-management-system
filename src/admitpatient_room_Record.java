    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Connection connection=null;
        DbHelper dbHelper=new DbHelper();
        PreparedStatement statement=null;
        try{
        connection = dbHelper.getConnection();
        String sql="insert into admitpatient_room(AdmitID,PatientID,Disease,AdmitDate,RoomNo,DoctorID,Remarks) values(?,?,?,?,?,?,?)";
        statement=connection.prepareStatement(sql);
        statement.setInt(1,Integer.valueOf(txtFileNumber.getText()));
        statement.setInt(2,Integer.valueOf(PatientID.getText()));
        statement.setString(3, txtDisease.getText());
        statement.setString(4, txtAdmitDate.getText());
        statement.setObject(5,(cmbRoomNo.getSelectedItem()));
        statement.setInt(6,Integer.valueOf(txtDoctorID.getText()));
        statement.setInt(7,Integer.valueOf(txtRemarks.getText()));
        int result=statement.executeUpdate();
        populateTable();
        
        
             
        }catch(SQLException exception){
            dbHelper.showErrorMessage(exception);
        }finally{
            try {
                statement.close();
                connection.close();
            } catch (SQLException ex) {
                Logger.getLogger(admitpatient_room_Record.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

