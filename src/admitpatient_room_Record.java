    public void populateTable(){
      
         model=(DefaultTableModel) tblHospital.getModel();
         model.setRowCount(0);
         
        try {
            ArrayList<admitpatient_room> admits=getAdmits();
            for(admitpatient_room admit:admits){
                Object[]row={admit.getAdmitID(),admit.getPatientID(),admit.getDisease(),admit.getAdmitDate(),
                    admit.getRoomNo(),admit.getDoctorID(),admit.getRemarks()};
                model.addRow(row);
            }
        } catch (SQLException ex) {
            
        }
    }
    public ArrayList<admitpatient_room> getAdmits() throws SQLException{
        Connection connection=null;
        DbHelper dbHelper=new DbHelper();
        Statement statement=null;
        ResultSet resultSet;
        ArrayList<admitpatient_room> Admits=null;
        try{
            connection=dbHelper.getConnection();
            statement=connection.createStatement();
            resultSet=statement.executeQuery("select*from admitpatient_room");
            Admits=new ArrayList<admitpatient_room>();
            while(resultSet.next()){
                Admits.add(new admitpatient_room(
                        resultSet.getInt("admitID"),
                        resultSet.getInt("PatientID"),
                        resultSet.getString("disease"),
                        resultSet.getString("AdmitDate"),
                        resultSet.getString("RoomNo"),
                        resultSet.getInt("DoctorID"),
                        resultSet.getInt("Remarks")));
            }
        }catch(SQLException exception){
            dbHelper.showErrorMessage(exception);
        
        }finally{
        statement.close();
        connection.close();
        }
        return Admits;
    }

